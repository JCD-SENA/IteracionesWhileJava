package juanca.iteracioneswhilejava;

/*
Ejercicio 2
Un programa que muestre los numeros pares del 0 al 100
*/

public class Ejercicio2 {
    public static void main(String[] args) {
        int i = 0;
        while (i <= 100) {
            System.out.println(i);
            i += 2;
        }
    }
}